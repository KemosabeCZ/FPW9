class Cat:
    def __init__(self, name, gen, age): # атрибуты класса
        self.name = name
        self.gen = gen
        self.age = age

    def getName(self):   # метод класса
        return self.name

    def getGen(self):   # метод класса
        return self.gen

    def getAge(self):    # метод класса
        return self.age

